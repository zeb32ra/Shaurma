public class Shava {
    public String lavash;
    public String salad;
    public String sause;
    public String cheese;
    public String tomatoes;
    public String meet;
    public int cost = 0;

    public Shava(String lav, String sal, String saus, String ches, String tomat, String m, int c)
    {
        lavash = lav;
        salad = sal;
        sause = saus;
        cheese = ches;
        tomatoes = tomat;
        meet = m;
        cost = c;
    }
}
